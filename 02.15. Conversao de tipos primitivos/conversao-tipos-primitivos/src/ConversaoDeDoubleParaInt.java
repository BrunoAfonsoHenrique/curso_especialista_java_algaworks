public class ConversaoDeDoubleParaInt {

    public static void main(String[] args) {
        double largura = 100;
        int tamanho = (int) largura;
        System.out.println(tamanho);

        System.out.println("-------");

        double largura2 = 100.37;
        int tamanho2 = (int) largura2;
        System.out.println(tamanho2);
    }
}
